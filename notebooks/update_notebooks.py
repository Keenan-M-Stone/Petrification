    "        gp = 1 + alpha * (fp - 1)\n"
    "        if abs(gp) < 1e-30:\n"
    "            return -60 / (np.pi * np.sqrt(x * (1 - x) + 1e-30))\n"
    "        return np.log(abs(gp)) / (np.pi * np.sqrt(x * (1 - x) + 1e-30))\n"
