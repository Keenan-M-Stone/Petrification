# Cell 1 (lyapunov function def)
code_cells[1][1]['source'] = to_lines(
    "def lyapunov(f, a, x0, n_iter=10000, n_discard=1000, alpha=None):\n"
    "    if alpha is not None:\n"
    "        traj = iterate_transformed(logistic, alpha, a, x0, n_iter)\n"
    "    else:\n"
    "        traj = iterate(logistic, a, x0, n_iter)\n"
    "    traj = traj[n_discard:]\n"
    "    f_prime = a * (1 - 2 * traj[:-1])\n"
    "    if alpha is not None:\n"
    "        g_prime = 1 + alpha * (f_prime - 1)\n"
    "    else:\n"
    "        g_prime = f_prime\n"
    "    gp_abs = np.maximum(np.abs(g_prime), 1e-30)\n"
    "    return np.mean(np.log(gp_abs))"
)

